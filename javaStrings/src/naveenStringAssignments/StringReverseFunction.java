package naveenStringAssignments;

public class StringReverseFunction {

	public static void main(String[] args) {
		
		StringBuilder b1 = new StringBuilder("sathappan");
		b1.reverse();
		System.out.println(b1);
		
		StringBuffer b2=new StringBuffer("ChennAI");
		b2.reverse();
		System.out.println(b2);
	}

}
