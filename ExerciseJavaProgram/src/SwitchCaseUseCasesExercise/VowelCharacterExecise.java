package SwitchCaseUseCasesExercise;

public class VowelCharacterExecise {

	public static void main(String[] args) {
		
		char alpha = 97;
		
		switch (alpha) {
		case 97:
			System.out.println(alpha + " is a vowel");
			break;
		case 101:
			System.out.println(alpha + " is a vowel");
			break;
		case 105:
			System.out.println(alpha + " is a vowel");
			break;
		case 111:
			System.out.println(alpha + " is a vowel");
			break;
		case 117:
			System.out.println(alpha + " is a vowel");
			break;
		default:
			System.out.println(alpha + " is not a vowel");
			break;
		}

	}

}
