/*Write a program to add 3 to the ASCII value of the character 'd' and print the equivalent character.*/
public class p8 {

	public static void main(String[] args) {
		char s1='d';
		byte s2=3;
		int sum=s1+s2;
		System.out.println(sum);
		System.out.println((char)sum);

	}

}
